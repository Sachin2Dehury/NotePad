    private void Find_AllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Find_AllActionPerformed
        try 
        {
            notepad.Notepad.Text_area.getSelectedText().equals("");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
        try 
        {
            int select_start=notepad.Notepad.Text_area.getText().indexOf(Find_TextField.getText());
            int select_end=select_start+Find_TextField.getText().length();
            notepad.Notepad.Text_area.select(select_start, select_end);
            start=select_end+1;
            if(select_start==notepad.Notepad.Text_area.getText().lastIndexOf(Find_TextField.getText()))
            {
                start=0;
            }
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Find_AllActionPerformed

    private void ReplaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReplaceActionPerformed
        try 
        {
            FindActionPerformed(evt);
            notepad.Notepad.Text_area.replaceSelection(Replace_TextField.getText());
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ReplaceActionPerformed

    private void Replace_AllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Replace_AllActionPerformed
        try 
        {
            Find_AllActionPerformed(evt);
            notepad.Notepad.Text_area.setText(notepad.Notepad.Text_area.getText().replaceAll(Find_TextField.getText(),Replace_TextField.getText()));
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Replace_AllActionPerformed

