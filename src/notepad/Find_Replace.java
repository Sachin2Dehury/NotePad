    private void FindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindActionPerformed
        int select_start=notepad.Notepad.Text_area.getText().indexOf(Find_TextField.getText());
        if(select_start==-1)
        {
            start=0;
            JOptionPane.showMessageDialog(null, "Could not Find !");
        }
        else if(select_start==notepad.Notepad.Text_area.getText().lastIndexOf(Find_TextField.getText()))
        {
            start=0;
        }
        int select_end=select_start+Find_TextField.getText().length();
        notepad.Notepad.Text_area.select(select_start, select_end);
    }//GEN-LAST:event_FindActionPerformed

